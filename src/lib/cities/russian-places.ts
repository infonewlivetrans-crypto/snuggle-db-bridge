/**
 * Локальный справочник населённых пунктов России.
 * Не использует внешние API. Не закрывает ручной ввод — если города нет
 * в списке, форма всё равно принимает строку и помечает её как
 * "не найден в справочнике".
 *
 * Список не претендует на полноту — это рабочая база. Расширяется при
 * необходимости. Покрывает: региональные центры, крупные города,
 * множество станиц, посёлков и сёл (особенно ЮФО и СКФО).
 */

export type PlaceKind =
  | "city" // город
  | "town" // ПГТ
  | "settlement" // посёлок
  | "village" // село / деревня
  | "stanitsa" // станица
  | "khutor"; // хутор

export interface RussianPlace {
  /** Название без типа. */
  name: string;
  /** Тип населённого пункта. */
  kind: PlaceKind;
  /** Регион (субъект РФ). */
  region: string;
}

// Удобный helper для кратких записей.
const c = (name: string, region: string): RussianPlace => ({ name, kind: "city", region });
const t = (name: string, region: string): RussianPlace => ({ name, kind: "town", region });
const s = (name: string, region: string): RussianPlace => ({ name, kind: "settlement", region });
const v = (name: string, region: string): RussianPlace => ({ name, kind: "village", region });
const st = (name: string, region: string): RussianPlace => ({ name, kind: "stanitsa", region });
const h = (name: string, region: string): RussianPlace => ({ name, kind: "khutor", region });

