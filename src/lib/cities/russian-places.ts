// Карта для быстрого нечувствительного к регистру поиска по точному имени.
const NORMALIZED_INDEX = new Map<string, RussianPlace>();
for (const p of RUSSIAN_PLACES) {
  const key = p.name.toLowerCase().replace(/ё/g, "е");
  if (!NORMALIZED_INDEX.has(key)) NORMALIZED_INDEX.set(key, p);
}

/** Знает ли справочник такое название (без учёта регистра, ё/е). */
export function isKnownPlace(name: string | null | undefined): boolean {
  if (!name) return false;
  return NORMALIZED_INDEX.has(name.toLowerCase().trim().replace(/ё/g, "е"));
}

/** Найти место по точному имени (без учёта регистра, ё/е). */
export function findPlace(name: string | null | undefined): RussianPlace | null {
  if (!name) return null;
  return NORMALIZED_INDEX.get(name.toLowerCase().trim().replace(/ё/g, "е")) ?? null;
}

/** Поиск по префиксу/подстроке. Возвращает до limit совпадений. */
export function searchPlaces(query: string, limit = 30): RussianPlace[] {
  const q = query.toLowerCase().trim().replace(/ё/g, "е");
  if (!q) {
    // По умолчанию — первые крупные города.
    return RUSSIAN_PLACES.slice(0, limit) as RussianPlace[];
  }
  const starts: RussianPlace[] = [];
  const contains: RussianPlace[] = [];
  for (const p of RUSSIAN_PLACES) {
    const n = p.name.toLowerCase().replace(/ё/g, "е");
    if (n.startsWith(q)) starts.push(p);
    else if (n.includes(q)) contains.push(p);
    if (starts.length >= limit) break;
  }
  return [...starts, ...contains].slice(0, limit);
}
